package Space_Invaders;

public class Spaceship {
	
	// the x and y coordinates of the Spaceship.
	private int x, y;
	
	/**
	 * the method that returns the x position
	 * 
	 * @return x position
	 */
	public int getPosX(){
		return x;
	}
	
	/**
	 * the method that returns the y position
	 * 
	 * @return y position
	 */
	public int getPosY(){
		return y;
	}
}
